// lib/features/auth/data/models/user_model.dart
import 'package:ekaplus_ekatunggal/features/auth/domain/entities/user.dart';

class UserModel extends User {
  const UserModel({
    required super.id,
    required super.firstName,
    required super.lastName,
    required super.fullName,
    super.username,
    required super.email,
    required super.phone,
    required super.password,
    super.isEmailVerified,
    super.isPhoneVerified,
    required super.gender,
    required super.dateOfBirth,
    required super.birthPlace,
    super.profilePic,
    super.picture,
    super.referralCode,
    super.referredBy,
    super.address,
    super.city,
    super.province,
    super.postalCode,
    super.country,
    super.googleId,
    super.googleAccessToken,
    super.googleRefreshToken,
    super.googleTokenExpiry,
    super.role,
    super.status,
    super.workflowState,
    super.tokenVersion,
    super.lastLogin,
    super.createdBy,
    super.updatedBy,
    required super.createdAt,
    required super.updatedAt,
    super.isSystem,
  });

  factory UserModel.fromJson(Map<String, dynamic> json) {
    return UserModel(
      id: json['id'] as String,
      firstName: json['first_name'] as String,
      lastName: json['last_name'] as String,
      fullName: json['full_name'] as String,
      username: json['username'] as String?,
      email: json['email'] as String,
      phone: json['phone'] as String,
      password: json['password'] as String,
      isEmailVerified: json['is_email_verified'] as bool? ?? false,
      isPhoneVerified: json['is_phone_verified'] as bool? ?? true,
      gender: json['gender'] as String,
      dateOfBirth: json['date_of_birth'] as String,
      birthPlace: json['birth_place'] as String,
      profilePic: json['profile_pic'] as String?,
      picture: json['picture'] as String?,
      referralCode: json['referral_code'] as String?,
      referredBy: json['referred_by'] as String?,
      address: json['address'] as String?,
      city: json['city'] as String?,
      province: json['province'] as String?,
      postalCode: json['postal_code'] as String?,
      country: json['country'] as String?,
      googleId: json['google_id'] as String?,
      googleAccessToken: json['google_access_token'] as String?,
      googleRefreshToken: json['google_refresh_token'] as String?,
      googleTokenExpiry: json['google_token_expiry'] != null
          ? DateTime.parse(json['google_token_expiry'] as String)
          : null,
      role: json['role'] as String? ?? 'user',
      status: json['status'] as String? ?? 'active',
      workflowState: json['workflow_state'] as String? ?? 'registered',
      tokenVersion: json['token_version'] as int? ?? 0,
      lastLogin: json['last_login'] != null
          ? DateTime.parse(json['last_login'] as String)
          : null,
      createdBy: json['created_by'] as String?,
      updatedBy: json['updated_by'] as String?,
      createdAt: DateTime.parse(json['created_at'] as String),
      updatedAt: DateTime.parse(json['updated_at'] as String),
      isSystem: json['is_system'] as bool? ?? false,
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'first_name': firstName,
      'last_name': lastName,
      'full_name': fullName,
      'username': username,
      'email': email,
      'phone': phone,
      'password': password,
      'is_email_verified': isEmailVerified,
      'is_phone_verified': isPhoneVerified,
      'gender': gender,
      'date_of_birth': dateOfBirth,
      'birth_place': birthPlace,
      'profile_pic': profilePic,
      'picture': picture,
      'referral_code': referralCode,
      'referred_by': referredBy,
      'address': address,
      'city': city,
      'province': province,
      'postal_code': postalCode,
      'country': country,
      'google_id': googleId,
      'google_access_token': googleAccessToken,
      'google_refresh_token': googleRefreshToken,
      'google_token_expiry': googleTokenExpiry?.toIso8601String(),
      'role': role,
      'status': status,
      'workflow_state': workflowState,
      'token_version': tokenVersion,
      'last_login': lastLogin?.toIso8601String(),
      'created_by': createdBy,
      'updated_by': updatedBy,
      'created_at': createdAt.toIso8601String(),
      'updated_at': updatedAt.toIso8601String(),
      'is_system': isSystem,
    };
  }

  UserModel copyWith({
    String? id,
    String? firstName,
    String? lastName,
    String? fullName,
    String? username,
    String? email,
    String? phone,
    String? password,
    bool? isEmailVerified,
    bool? isPhoneVerified,
    String? gender,
    String? dateOfBirth,
    String? birthPlace,
    String? profilePic,
    String? picture,
    String? referralCode,
    String? referredBy,
    String? address,
    String? city,
    String? province,
    String? postalCode,
    String? country,
    String? googleId,
    String? googleAccessToken,
    String? googleRefreshToken,
    DateTime? googleTokenExpiry,
    String? role,
    String? status,
    String? workflowState,
    int? tokenVersion,
    DateTime? lastLogin,
    String? createdBy,
    String? updatedBy,
    DateTime? createdAt,
    DateTime? updatedAt,
    bool? isSystem,
  }) {
    return UserModel(
      id: id ?? this.id,
      firstName: firstName ?? this.firstName,
      lastName: lastName ?? this.lastName,
      fullName: fullName ?? this.fullName,
      username: username ?? this.username,
      email: email ?? this.email,
      phone: phone ?? this.phone,
      password: password ?? this.password,
      isEmailVerified: isEmailVerified ?? this.isEmailVerified,
      isPhoneVerified: isPhoneVerified ?? this.isPhoneVerified,
      gender: gender ?? this.gender,
      dateOfBirth: dateOfBirth ?? this.dateOfBirth,
      birthPlace: birthPlace ?? this.birthPlace,
      profilePic: profilePic ?? this.profilePic,
      picture: picture ?? this.picture,
      referralCode: referralCode ?? this.referralCode,
      referredBy: referredBy ?? this.referredBy,
      address: address ?? this.address,
      city: city ?? this.city,
      province: province ?? this.province,
      postalCode: postalCode ?? this.postalCode,
      country: country ?? this.country,
      googleId: googleId ?? this.googleId,
      googleAccessToken: googleAccessToken ?? this.googleAccessToken,
      googleRefreshToken: googleRefreshToken ?? this.googleRefreshToken,
      googleTokenExpiry: googleTokenExpiry ?? this.googleTokenExpiry,
      role: role ?? this.role,
      status: status ?? this.status,
      workflowState: workflowState ?? this.workflowState,
      tokenVersion: tokenVersion ?? this.tokenVersion,
      lastLogin: lastLogin ?? this.lastLogin,
      createdBy: createdBy ?? this.createdBy,
      updatedBy: updatedBy ?? this.updatedBy,
      createdAt: createdAt ?? this.createdAt,
      updatedAt: updatedAt ?? this.updatedAt,
      isSystem: isSystem ?? this.isSystem,
    );
  }
}